read_sample=function(distName,n,m,sds){

  distr_y <- paste0("G1 and G2 when distr=",distName,", ")
  nobs_y <- paste0("n=",n,", ")
  mu_y <- paste0("means=",m)
  std_y <- paste0(", sds=",sds)

  fname <-  paste0(distr_y, nobs_y, mu_y,std_y,".rds")
  
  setwd(dir="E:/Welch's W ANOVA/Outputs Appendix/Appendix 5_G1 and G2/") # destination file
  data=readRDS(file = fname)
  return(data)
}

